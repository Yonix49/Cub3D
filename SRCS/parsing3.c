#include "Cub3D.h"

int	is_char_sett(char *map)
{
	int	i;

	i = 0;
	while (map[i])
	{
		if (map[0] == '\n' || ft_strlen(map) < 3 || (map[i] == '\n' && map[i
					+ 1] == '\n'))
		{
			write(2, "Error\nSpace", 12);
			return (0);
		}
		i++;
	}
	return (9999);
}

