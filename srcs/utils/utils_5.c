#include "../Cub3D.h"

int	ft_space(char c)
{
	if ((c >= 9 && c <= 13) || c == 32)
		return (1);
	return (0);
}

int	ft_count_space(char *line)
{
	int	i;

	i = 0;
	while (ft_space(line[i]))
		i++;
	return (i);
}
